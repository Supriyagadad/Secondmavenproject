package com.tpointprgrams;

public class Breakclass {

	public static void main(String[] args) {

		int number=20;
		switch (number) 
		{
		case 10:
			System.out.println("10");
			break;
		case 20:
			System.out.println("10");
			break;
		case 30:
			System.out.println("10");
			break;
		default:
			System.out.println("Not in 10,20,30");
			break;
		}
	}

}
